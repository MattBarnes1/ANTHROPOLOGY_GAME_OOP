package anthropologyapplication.Warriors;

import anthropologyapplication.Timer;
import anthropologyapplication.Warriors.Warrior;

/**
 *
 * @author noone
 */
public class Clubmen extends Warrior {

    public Clubmen(String Name, String Description,  Timer TrainingTimeMS, int Strength)
    { 
        super(Name, Description,  TrainingTimeMS, Strength);
    }
    
    @Override
    public Warrior Copy()
    {
       return new Clubmen(this);
    }
    
    
    private Clubmen(Clubmen myClubbers)
    {
        super(myClubbers.getName(), myClubbers.getDescription(), myClubbers.BuildTime, myClubbers.Strength);
    }
    
    
}
